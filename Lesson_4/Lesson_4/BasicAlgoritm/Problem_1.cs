using System;
using System.Collections.Generic;
using System.Text;

namespace Lesson_4
{
    class Problem_1
    {
        public static int Solve(int a, int b)
        {
            if (a == b)
            {
                return (a + b) * 3;
            }
            else
            {
                return a + b;
            }
        }
    }
}
